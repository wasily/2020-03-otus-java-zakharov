package ru.otus.hw10.core.model;

import ru.otus.hw10.jdbc.mapper.annotations.Id;

/**
 * @author sergey
 * created on 03.02.19.
 */
public class User {
    @Id
    private long id;
    private String name;

    public User() {
    }

    public User(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getId() {
        return id;
    }

    public String getName() {
        return name;
    }


    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
